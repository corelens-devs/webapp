// Function to get product details by category and slug
export const getProductDetails = (category, slug) => {
  const categoryData = categoryProductDetails[category];
  if (!categoryData) {
    return null;
  }
  return categoryData[slug] || null;
};

// Function to get all products in a category
export const getCategoryProducts = (category) => {
  const categoryData = categoryProductDetails[category];
  if (!categoryData) {
    return [];
  }
  return Object.values(categoryData);
};

// Function to check if product exists
export const productExists = (category, slug) => {
  return getProductDetails(category, slug) !== null;
};
